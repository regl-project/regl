  // ===================================================
  // SCOPE COMMAND
  // ===================================================
  // ===================================================
  function emitScopeProc (env, args) {
    var scope = env.proc('scope', 3)
    env.batchId = 'a2'

    var shared = env.shared
    var CURRENT_STATE = shared.current

    emitContext(env, scope, args.context)

    if (args.framebuffer) {
      args.framebuffer.append(env, scope)
    }

    sortState(Object.keys(args.state)).forEach(function (name) {
      var defn = args.state[name]
      var value = defn.append(env, scope)
      if (isArrayLike(value)) {
        value.forEach(function (v, i) {
          if (hasVariableReference(v)) {
            scope.set(env.next[name], '[' + i + ']', v)
          } else {
            scope.set(env.next[name], '[' + i + ']', env.link(v, {stable: true}))
          }
        })
      } else {
        if (isStatic(defn)) {
          scope.set(shared.next, '.' + name, env.link(value, {stable: true}))
        } else {
          scope.set(shared.next, '.' + name, value)
        }
      }
    })

    emitProfile(env, scope, args, true, true)

    ;[S_ELEMENTS, S_OFFSET, S_COUNT, S_INSTANCES, S_PRIMITIVE].forEach(
      function (opt) {
        var variable = args.draw[opt]
        if (!variable) {
          return
        }
        var VARIABLE = variable.append(env, scope)
        if (hasVariableReference(VARIABLE)) {
          scope.set(shared.draw, '.' + opt, VARIABLE)
        } else {
          scope.set(shared.draw, '.' + opt, env.link(VARIABLE), {stable: true})
        }
      })

    Object.keys(args.uniforms).forEach(function (opt) {
      var value = args.uniforms[opt].append(env, scope)
      if (Array.isArray(value)) {
        value = '[' + value.map(function (v) {
          if (hasVariableReference(v)) {
            return v;
          } else {
            return env.link(v, {stable: true})
          }
        }) + ']'
      }
      scope.set(
        shared.uniforms,
        '[' + env.link(stringStore.id(opt), {stable: true}) + ']',
        value)
    })

    Object.keys(args.attributes).forEach(function (name) {
      var record = args.attributes[name].append(env, scope)
      var scopeAttrib = env.scopeAttrib(name)
      Object.keys(new AttributeRecord()).forEach(function (prop) {
        scope.set(scopeAttrib, '.' + prop, record[prop])
      })
    })

    if (args.scopeVAO) {
      var VARIABLE = args.scopeVAO.append(env, scope)
      if (hasVariableReference(VARIABLE)) {
        scope.set(shared.vao, '.targetVAO', VARIABLE)
      } else {
        scope.set(shared.vao, '.targetVAO', env.link(VARIABLE, {stable: true}))
      }
    }

    function saveShader (name) {
      var shader = args.shader[name]
      if (shader) {
        var VARIABLE = shader.append(env, scope)
        if (hasVariableReference(VARIABLE)) {
          scope.set(shared.shader, '.' + name, VARIABLE)
        } else {
          scope.set(shared.shader, '.' + name, env.link(VARIABLE, {stable: true}))
        }
      }
    }
    saveShader(S_VERT)
    saveShader(S_FRAG)

    if (Object.keys(args.state).length > 0) {
      scope(CURRENT_STATE, '.dirty=true;')
      scope.exit(CURRENT_STATE, '.dirty=true;')
    }

    scope('a1(', env.shared.context, ',a0,', env.batchId, ');')
  }

  function isDynamicObject (object) {
    if (typeof object !== 'object' || isArrayLike(object)) {
      return
    }
    var props = Object.keys(object)
    for (var i = 0; i < props.length; ++i) {
      if (dynamic.isDynamic(object[props[i]])) {
        return true
      }
    }
    return false
  }

  function splatObject (env, options, name) {
    var object = options.static[name]
    if (!object || !isDynamicObject(object)) {
      return
    }

    var globals = env.global
    var keys = Object.keys(object)
    var thisDep = false
    var contextDep = false
    var propDep = false
    var objectRef = env.global.def('{}')
    keys.forEach(function (key) {
      var value = object[key]
      if (dynamic.isDynamic(value)) {
        if (typeof value === 'function') {
          value = object[key] = dynamic.unbox(value)
        }
        var deps = createDynamicDecl(value, null)
        thisDep = thisDep || deps.thisDep
        propDep = propDep || deps.propDep
        contextDep = contextDep || deps.contextDep
      } else {
        globals(objectRef, '.', key, '=')
        switch (typeof value) {
          case 'number':
            globals(value)
            break
          case 'string':
            globals('"', value, '"')
            break
          case 'object':
            if (Array.isArray(value)) {
              globals('[', value.join(), ']')
            }
            break
          default:
            globals(env.link(value))
            break
        }
        globals(';')
      }
    })

    function appendBlock (env, block) {
      keys.forEach(function (key) {
        var value = object[key]
        if (!dynamic.isDynamic(value)) {
          return
        }
        var ref = env.invoke(block, value)
        block(objectRef, '.', key, '=', ref, ';')
      })
    }

    options.dynamic[name] = new dynamic.DynamicVariable(DYN_THUNK, {
      thisDep: thisDep,
      contextDep: contextDep,
      propDep: propDep,
      ref: objectRef,
      append: appendBlock
    })
    delete options.static[name]
  }

  // ===========================================================================
  // ===========================================================================
  // MAIN DRAW COMMAND
  // ===========================================================================
  // ===========================================================================
  function compileCommand (options, attributes, uniforms, context, stats) {
    var env = createREGLEnvironment()

    // link stats, so that we can easily access it in the program.
    env.stats = env.link(stats)

    // splat options and attributes to allow for dynamic nested properties
    Object.keys(attributes.static).forEach(function (key) {
      splatObject(env, attributes, key)
    })
    NESTED_OPTIONS.forEach(function (name) {
      splatObject(env, options, name)
    })

    var args = parseArguments(options, attributes, uniforms, context, env)

    if (args.shader.program) {
      args.shader.program.attributes.sort(function (a, b) {
        return a.name < b.name ? -1 : 1
      })
      args.shader.program.uniforms.sort(function (a, b) {
        return a.name < b.name ? -1 : 1
      })
    }

    emitDrawProc(env, args)
    emitScopeProc(env, args)
    emitBatchProc(env, args)

    return extend(env.compile(), {
      destroy: function () {
        args.shader.program.destroy()
      }
    })
  }

  // ===========================================================================
  // ===========================================================================
  // POLL / REFRESH
  // ===========================================================================
  // ===========================================================================
  return {
    next: nextState,
    current: currentState,
    procs: (function () {
      var env = createREGLEnvironment()
      var poll = env.proc('poll')
      var refresh = env.proc('refresh')
      var common = env.block()
      poll(common)
      refresh(common)

      var shared = env.shared
      var GL = shared.gl
      var NEXT_STATE = shared.next
      var CURRENT_STATE = shared.current

      common(CURRENT_STATE, '.dirty=false;')

      emitPollFramebuffer(env, poll)
      emitPollFramebuffer(env, refresh, null, true)

      // Refresh updates all attribute state changes
      var INSTANCING
      if (extInstancing) {
        INSTANCING = env.link(extInstancing)
      }

      // update vertex array bindings
      if (extensions.oes_vertex_array_object) {
        refresh(env.link(extensions.oes_vertex_array_object), '.bindVertexArrayOES(null);')
      }
      var BINDING = refresh.def(shared.attributes)
      var TEMP_BINDING = refresh.def(0)

      var ifte = env.cond(TEMP_BINDING, '.buffer')
      ifte.then(
        GL, '.enableVertexAttribArray(i);',
        GL, '.bindBuffer(',
        GL_ARRAY_BUFFER, ',',
        TEMP_BINDING, '.buffer.buffer);',
        GL, '.vertexAttribPointer(i,',
        TEMP_BINDING, '.size,',
        TEMP_BINDING, '.type,',
        TEMP_BINDING, '.normalized,',
        TEMP_BINDING, '.stride,',
        TEMP_BINDING, '.offset);'
      ).else(
        GL, '.disableVertexAttribArray(i);',
        GL, '.vertexAttrib4f(i,',
        TEMP_BINDING, '.x,',
        TEMP_BINDING, '.y,',
        TEMP_BINDING, '.z,',
        TEMP_BINDING, '.w);',
        TEMP_BINDING, '.buffer=null;')
      var MAX_ATTRIBUTES = env.link(limits.maxAttributes, {stable: true})
      refresh(
        'for(var i=0;i<', MAX_ATTRIBUTES, ';++i){',
        TEMP_BINDING, '=', BINDING, '[i];',
        ifte,
        '}'
      )

      if (extInstancing) {
        refresh(
          'for(var i=0;i<', MAX_ATTRIBUTES, ';++i){',
          INSTANCING, '.vertexAttribDivisorANGLE(i,',
          BINDING, '[i].divisor);',
          '}')
      }
      refresh(
        env.shared.vao, '.currentVAO=null;',
        env.shared.vao, '.setVAO(', env.shared.vao, '.targetVAO);')

      Object.keys(GL_FLAGS).forEach(function (flag) {
        var cap = GL_FLAGS[flag]
        var NEXT = common.def(NEXT_STATE, '.', flag)
        var block = env.block()
        block('if(', NEXT, '){',
          GL, '.enable(', cap, ')}else{',
          GL, '.disable(', cap, ')}',
          CURRENT_STATE, '.', flag, '=', NEXT, ';')
        refresh(block)
        poll(
          'if(', NEXT, '!==', CURRENT_STATE, '.', flag, '){',
          block,
          '}')
      })

      Object.keys(GL_VARIABLES).forEach(function (name) {
        var func = GL_VARIABLES[name]
        var init = currentState[name]
        var NEXT, CURRENT
        var block = env.block()
        block(GL, '.', func, '(')
        if (isArrayLike(init)) {
          var n = init.length
          NEXT = env.global.def(NEXT_STATE, '.', name)
          CURRENT = env.global.def(CURRENT_STATE, '.', name)
          block(
            loop(n, function (i) {
              return NEXT + '[' + i + ']'
            }), ');',
            loop(n, function (i) {
              return CURRENT + '[' + i + ']=' + NEXT + '[' + i + '];'
            }).join(''))
          poll(
            'if(', loop(n, function (i) {
              return NEXT + '[' + i + ']!==' + CURRENT + '[' + i + ']'
            }).join('||'), '){',
            block,
            '}')
        } else {
          NEXT = common.def(NEXT_STATE, '.', name)
          CURRENT = common.def(CURRENT_STATE, '.', name)
          block(
            NEXT, ');',
            CURRENT_STATE, '.', name, '=', NEXT, ';')
          poll(
            'if(', NEXT, '!==', CURRENT, '){',
            block,
            '}')
        }
        refresh(block)
      })

      return env.compile()
    })(),
    compile: compileCommand
  }
